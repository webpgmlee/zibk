package or.kr.kbiz.bo.common.domain;

public class BatchLogDomain {
	private String batchJobId;
	private String batchResult;
	/**
	 * @return the batchJobId
	 */
	public String getBatchJobId() {
		return batchJobId;
	}
	/**
	 * @param batchJobId the batchJobId to set
	 */
	public void setBatchJobId(String batchJobId) {
		this.batchJobId = batchJobId;
	}
	/**
	 * @return the batchResult
	 */
	public String getBatchResult() {
		return batchResult;
	}
	/**
	 * @param batchResult the batchResult to set
	 */
	public void setBatchResult(String batchResult) {
		this.batchResult = batchResult;
	}
}
