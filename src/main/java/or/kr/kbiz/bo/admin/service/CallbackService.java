package or.kr.kbiz.bo.admin.service;

import java.util.List;

import or.kr.kbiz.bo.admin.domain.AdminCallbackDomain;
/**
 * <pre>CallbackService</pre>
 *
 * @author bsy
 * @since 2017.11.20
 * @version 1.0
 */
public interface CallbackService {

	/**
	 * 목록 조회 템플릿
	 *
	 * @param param
	 * @return List<CallbackDomain>
	 */
	abstract List<AdminCallbackDomain> selectCallbackList(AdminCallbackDomain param);

	/**
	 * 상세목록 조회 템플릿
	 *
	 * @param param
	 * @return CallbackDomain
	 */
	abstract Object selectCallbackDetail(AdminCallbackDomain param);

	/**
	 * 인입채널 조회 템플릿
	 *
	 * @param param
	 * @return List<AdminCallbackDomain>
	 */
	abstract List<AdminCallbackDomain> selectChCodeList(AdminCallbackDomain param);

	/**
	 * 목록(엑셀) 조회 템플릿
	 *
	 * @param param
	 * @return List<AdminCallbackDomain>
	 */
	abstract List<AdminCallbackDomain> selectCallbackExcelList(AdminCallbackDomain param);
}
